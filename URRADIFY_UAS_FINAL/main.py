    def load_songs_from_folder(self):
        self.songs.clear()
        self.all_mp3_files = []
        music_folder = os.path.join(BASE_DIR, "music")
        if os.path.exists(music_folder):
            for file in os.listdir(music_folder):
                if file.lower().endswith(".mp3"):
                    self.all_mp3_files.append(file)
                    self.songs.addItem(file)

    def filter_playlist(self, text):
        self.songs.clear()
        for file in self.all_mp3_files:
            if text.lower() in file.lower():
                self.songs.addItem(file)

    def song_selected(self, item):
        self.current_song = os.path.join(BASE_DIR, "music", item.text())
        self.now_playing.setText(f"🎵 Playing: {item.text()}")
        
        self.is_paused = False  
        path = os.path.abspath(self.current_song)
        self.player.setSource(QUrl.fromLocalFile(path))
        self.player.play()

        song_base_name = os.path.splitext(item.text())[0]
        cover_png = os.path.join(BASE_DIR, "assets", song_base_name + ".png")
        cover_jpg = os.path.join(BASE_DIR, "assets", song_base_name + ".jpg")

        if os.path.exists(cover_png):
            self.cover_label.setPixmap(QPixmap(cover_png).scaled(260, 260, Qt.AspectRatioMode.KeepAspectRatio, Qt.TransformationMode.SmoothTransformation))
        elif os.path.exists(cover_jpg):
            self.cover_label.setPixmap(QPixmap(cover_jpg).scaled(260, 260, Qt.AspectRatioMode.KeepAspectRatio, Qt.TransformationMode.SmoothTransformation))
        else:
            self.cover_label.clear()
            self.cover_label.setText("No Album Cover")

    def play_music(self):
        if self.current_song:
            self.player.play()
            self.is_paused = False
        elif self.songs.count() > 0:
            self.songs.setCurrentRow(0)
            self.song_selected(self.songs.currentItem())

    def pause_music(self):
        if self.player.playbackState() == QMediaPlayer.PlaybackState.PlayingState:
            self.player.pause()
            self.is_paused = True

    def stop_music(self):
        self.player.stop()
        self.is_paused = False
        self.progress_slider.setValue(0)

    def play_next_song(self):
        current_row = self.songs.currentRow()
        next_row = current_row + 1
        if next_row < self.songs.count():
            self.songs.setCurrentRow(next_row)
            self.song_selected(self.songs.currentItem())
        else:
            if self.songs.count() > 0:
                self.songs.setCurrentRow(0)
                self.song_selected(self.songs.currentItem())

    def play_prev_song(self):
        current_row = self.songs.currentRow()
        prev_row = current_row - 1
        if prev_row >= 0:
            self.songs.setCurrentRow(prev_row)
            self.song_selected(self.songs.currentItem())

    def handle_media_status(self, status):
        if status == QMediaPlayer.MediaStatus.EndOfMedia:
            self.play_next_song()

    def update_position(self, position):
        if not self.progress_slider.isSliderDown():
            self.progress_slider.setValue(position)
        self.update_time_label(position, self.player.duration())

    def update_duration(self, duration):
        self.progress_slider.setRange(0, duration)
        self.update_time_label(self.player.position(), duration)

    def set_music_position(self, position):
        self.player.setPosition(position)

    def update_time_label(self, position, duration):
        self.time_label.setText(f"{format_time(position)} / {format_time(duration)}")

    def change_volume(self, value):
        self.audio_output.setVolume(value / 100)

    def media_error(self, error, message):
        print("Multimedia Player Error:", message)

    def handle_logout(self):
        self.stop_music()
        self.controller.go_to_login()


