# Fungsi helper untuk mengubah milidetik menjadi format waktu MM:SS
def format_time(ms):
    seconds = int((ms / 1000) % 60)
    minutes = int((ms / (1000 * 60)) % 60)
    return f"{minutes:02d}:{seconds:02d}"

